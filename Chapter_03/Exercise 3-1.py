# Get the number.
number = int(input('Enter an integer: '))

# Determine if the number is positive, negative or zero.
if number > 0:
    print('Positive')
elif number < 0:
    print('Negative')
else:
    print('Zero')

# Determine if the number is even or odd.
if number % 2 == 0:
    print('Even')
else:
    print('Odd')
    


